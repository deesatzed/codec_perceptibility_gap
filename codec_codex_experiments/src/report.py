from __future__ import annotations

import json
import platform
import subprocess
import sys
from pathlib import Path
from typing import Any, Dict, List

import matplotlib.pyplot as plt
import numpy as np


def _json(obj: Any) -> str:
    return json.dumps(obj, sort_keys=True)


def _table(headers: List[str], rows: List[List[Any]]) -> str:
    out = ["| " + " | ".join(headers) + " |", "|" + "|".join(["---"] * len(headers)) + "|"]
    for row in rows:
        out.append("| " + " | ".join(str(x) for x in row) + " |")
    return "\n".join(out)


def environment_summary() -> Dict[str, str]:
    try:
        import sklearn
        sklearn_version = sklearn.__version__
    except Exception:
        sklearn_version = "unknown"
    try:
        import numpy
        numpy_version = numpy.__version__
    except Exception:
        numpy_version = "unknown"
    return {
        "python": sys.version.split()[0],
        "platform": platform.platform(),
        "processor": platform.processor() or "unknown",
        "numpy": numpy_version,
        "scikit_learn": sklearn_version,
    }


def write_plots(results: Dict[str, Any], outdir: Path) -> List[str]:
    figdir = outdir / "figures"
    figdir.mkdir(parents=True, exist_ok=True)
    created: List[str] = []

    # CodecBench recovery curve, averaged from first seed if available.
    try:
        seed0 = results["codec_contest"]["codec_robustness"]["per_seed"][0]
        ks = [str(k) for k in seed0["codec_ladder_k"]]
        x = np.arange(len(ks))
        g = [seed0["grounded_channel"]["recovery_rho"][k] for k in ks]
        u = [seed0["uninformative_or_fluency_only_channel"]["recovery_rho"][k] for k in ks]
        plt.figure(figsize=(6, 4))
        plt.plot(x, g, marker="o", label="grounded")
        plt.plot(x, u, marker="o", label="uninformative")
        plt.xticks(x, ks)
        plt.xlabel("codec distinction count k")
        plt.ylabel("recovery rho")
        plt.title("CodecBench recovery curve, seed 0")
        plt.legend()
        p = figdir / "codecbench_recovery_curve.png"
        plt.tight_layout()
        plt.savefig(p, dpi=160)
        plt.close()
        created.append(str(p.relative_to(outdir)))
    except Exception:
        pass

    # Magnetic pendulum accuracy by magnet count.
    try:
        by_m = results["magnetic_pendulum"]["by_magnet_count"]
        ms = sorted(by_m, key=lambda z: int(z))
        x = np.arange(len(ms))
        native = [by_m[m]["native_accuracy_mean"] for m in ms]
        expanded = [by_m[m]["expanded_physics_accuracy_mean"] for m in ms]
        direct = [by_m[m]["direct_accuracy_mean"] for m in ms]
        leak = [by_m[m].get("oracle_leak_positive_control_accuracy_mean") for m in ms]
        plt.figure(figsize=(6, 4))
        plt.plot(x, native, marker="o", label="native")
        plt.plot(x, expanded, marker="o", label="expanded physics")
        plt.plot(x, direct, marker="o", label="direct")
        if all(v is not None for v in leak):
            plt.plot(x, leak, marker="x", linestyle="--", label="oracle-leak control")
        plt.xticks(x, ms)
        plt.xlabel("number of magnets")
        plt.ylabel("basin-label accuracy")
        plt.title("Magnetic-pendulum basin prediction")
        plt.legend()
        p = figdir / "magnetic_basin_accuracy.png"
        plt.tight_layout()
        plt.savefig(p, dpi=160)
        plt.close()
        created.append(str(p.relative_to(outdir)))
    except Exception:
        pass

    return created


def write_report(results: Dict[str, Any], outdir: Path, commands: List[str] | None = None) -> Path:
    outdir.mkdir(parents=True, exist_ok=True)
    figs = write_plots(results, outdir)
    env = results.get("environment", environment_summary())
    commands = commands or []

    lines: List[str] = []
    lines.append("# Codec Contest Experiment Report")
    lines.append("")
    lines.append("Generated by `python -m src.run_all`. Treat this as a simulation/software smoke-test report unless the run was preregistered and frozen in advance.")
    lines.append("")

    lines.append("## Environment")
    lines.append(_table(["field", "value"], [[k, v] for k, v in env.items()]))
    lines.append("")

    lines.append("## Commands")
    if commands:
        for cmd in commands:
            lines.append(f"- `{cmd}`")
    else:
        lines.append("- Commands not recorded.")
    lines.append("")

    lines.append("## 1. Proof ladder")
    ladder = results.get("proof_ladder", {})
    rows = []
    for s in ["S0", "S1", "S2", "S3", "S4", "S5", "S6", "S7", "S8"]:
        r = ladder.get(s, {})
        rows.append([s, r.get("name", ""), "PASS" if r.get("passed") else "FAIL", _json({k: v for k, v in r.items() if k not in {"name", "passed"}})])
    lines.append(_table(["stage", "claim", "gate", "metrics"], rows))
    lines.append("")

    # critic1 #1: parallel verification tracks. The same gates, regrouped into
    # three orthogonal lanes so a failing lane does not marginalize an unrelated one.
    tracks = ladder.get("tracks")
    if tracks:
        lines.append("### 1a. Parallel verification tracks (decoupled lanes)")
        track_rows = []
        for lane, t in tracks.items():
            track_rows.append([
                lane,
                ", ".join(t.get("gates", [])),
                "PASS" if t.get("lane_passed") else "FAIL",
                f"{t.get('n_passed')}/{t.get('n_gates')}",
                t.get("description", ""),
            ])
        lines.append(_table(["lane", "gates", "lane gate", "passed", "scope"], track_rows))
        lines.append("")
        lines.append("Lanes are evaluated independently: a quirk in the complexity/scaling lane (e.g. the S7 dimensionality slope) does not invalidate the orthogonal auditability lane (S5/S8). Gate thresholds are unchanged from the serial ladder; only the grouping differs.")
        lines.append("")

    lines.append("## 2. CodecGuard: multi-codec consistency audit")
    audit = results.get("codec_contest", {}).get("multi_codec_audit", {})
    audit_rows = [[k, v] for k, v in audit.items() if k not in {"per_seed", "interpretation"}]
    lines.append(_table(["metric", "value"], audit_rows))
    lines.append("")
    lines.append("Interpretation: disagreement is useful only insofar as it predicts true error and enriches high-error cases. High pairwise codec-error correlation is a correlated-error warning, not a success.")
    lines.append("")

    lines.append("## 3. CodecBench: codec-robustness curve")
    bench = results.get("codec_contest", {}).get("codec_robustness", {})
    bench_rows = [[k, v] for k, v in bench.items() if k not in {"per_seed", "interpretation"}]
    lines.append(_table(["metric", "value"], bench_rows))
    lines.append("")
    lines.append("Interpretation: a good channel should retain recoverable structure across the codec ladder. A bad-and-flat channel is not robust; it is uninformative.")
    lines.append("")

    lines.append("## 4. Magnetic-pendulum nonlinear stress test")
    mag = results.get("magnetic_pendulum", {})
    mag_rows = []
    for m, r in mag.get("by_magnet_count", {}).items():
        mag_rows.append([
            m,
            r.get("native_accuracy_mean"),
            r.get("expanded_physics_accuracy_mean"),
            r.get("direct_accuracy_mean"),
            r.get("oracle_leak_positive_control_accuracy_mean"),
            r.get("native_divergence_nrmse_mean"),
            r.get("expanded_physics_divergence_nrmse_mean"),
            r.get("direct_divergence_nrmse_mean"),
            r.get("basin_entropy_norm_mean"),
        ])
    lines.append(_table([
        "magnets", "native acc", "expanded-physics acc", "direct acc", "oracle-leak ctrl acc",
        "native div nRMSE", "expanded-physics div nRMSE", "direct div nRMSE", "entropy"
    ], mag_rows))
    lines.append("")
    lines.append("Note: `expanded-physics` adds only real omitted observables (no label/divergence target). `oracle-leak ctrl` is a leakage positive control whose accuracy ceiling is ~(1 − flip_prob); it is NOT evidence of physical access (see critical_review.md F1).")
    lines.append("")
    lines.append(f"Slope gate: {'PASS' if mag.get('slope_gate_passed') else 'FAIL'} — {mag.get('slope_gate_note', '')}")
    cond = mag.get("slope_gate_conditions")
    if cond:
        lines.append("")
        lines.append("Gate conditions: " + _json(cond))
    lines.append("")

    xf = results.get("cross_family")
    if xf:
        lines.append("## 5. Cross-family generalization (F5)")
        families = xf.get("families_run", [])
        gm = xf.get("gate_matrix", {})
        per_gate = gm.get("per_gate_pass_by_family", {})
        gate_rows = []
        for gate in ["S0", "S1", "S2", "S3", "S4", "S5", "S6", "S7", "S8"]:
            fam_results = per_gate.get(gate, {})
            row = [gate] + ["PASS" if fam_results.get(f) else "FAIL" for f in families]
            gate_rows.append(row)
        lines.append(_table(["gate"] + families, gate_rows))
        lines.append("")
        lines.append(f"**Family-robust gates (PASS on >=2 families):** {', '.join(gm.get('family_robust_gates', [])) or 'none'}")
        lines.append(f"**Family-specific gates (PASS on one family):** {', '.join(gm.get('family_specific_gates', [])) or 'none'}")
        lines.append(f"**Failing on all families:** {', '.join(gm.get('failing_on_all_families', [])) or 'none'}")
        lines.append("")
        lines.append("Interpretation: family-robust gates demonstrate the measurement logic generalizes beyond the single linear calibration source. A gate failing on all families is an honest negative; thresholds were not tuned per family.")
        lines.append("")

        # critic1 #3: CodecGuard audit survival on each family, including the
        # non-oscillatory chaotic family. This is the headline cross-family test.
        survival = xf.get("codecguard_audit_survival", {})
        if survival:
            lines.append("### 5a. CodecGuard audit survival across dynamical classes")
            srows = []
            for fam, s in survival.items():
                srows.append([
                    fam,
                    s.get("naive_corr"),
                    s.get("loco_corr"),
                    s.get("all_seeds_clear_permutation_null"),
                    s.get("catch_rate"),
                    s.get("random_catch_baseline"),
                    "YES" if s.get("audit_signal_survives") else "NO",
                ])
            lines.append(_table(
                ["family", "naive corr", "LOCO corr", "clears null (all seeds)", "catch rate", "catch baseline", "audit survives"],
                srows,
            ))
            lines.append("")
            lines.append("`henon_map` is a discrete-time chaotic family with no continuous momentum or periodic-attractor symmetry (critic1 #3). The hardened **LOCO** correlation and the worst-error **catch rate** are the physics-independent survival signals; the naive correlation and the permutation-null clearance can degrade on the chaotic family, and that degradation is reported as-is rather than tuned away. Chaos parameters themselves are largely unrecoverable from coarse orbit statistics (sensitive dependence), so the ladder's parameter-recovery gates fail on `henon_map` by construction -- a real limit of auditing alien dynamics, not a pipeline defect.")
            lines.append("")

    bv = results.get("bridging_validation")
    if bv:
        lines.append("## 6. Bridging validation: estimator-class sensitivity (critic1 #2)")
        lines.append("")
        lines.append("Ridge (developmental) vs MLP (proposed confirmatory) on the **exposed developmental families only** -- the reserved confirmatory holdout is never read (`holdout_touched = " + str(bv.get("holdout_touched")) + "`). This anchors a provisional machine ceiling and tests whether the throughput-invariance band `delta_eq = " + str(bv.get("delta_eq")) + "` is sensitive to estimator class before any pre-registration freeze.")
        lines.append("")
        bv_rows = []
        for fam, v in bv.get("per_family", {}).items():
            be = v.get("by_estimator", {})
            bv_rows.append([
                fam,
                be.get("ridge", {}).get("direct_ceiling_mean"),
                be.get("mlp", {}).get("direct_ceiling_mean"),
                v.get("ceiling_shift_ridge_to_mlp"),
                f"{v.get('ceiling_shift_pct')}%",
                be.get("ridge", {}).get("throughput_delta_max"),
                be.get("mlp", {}).get("throughput_delta_max"),
                "YES" if v.get("margin_fragile") else "no",
            ])
        lines.append(_table(
            ["family", "ridge ceiling", "mlp ceiling", "shift", "shift %", "ridge thru-delta max", "mlp thru-delta max", "margin fragile"],
            bv_rows,
        ))
        lines.append("")
        lines.append(f"**Any margin fragile:** {bv.get('any_margin_fragile')} — **max abs ceiling shift:** {bv.get('max_abs_ceiling_shift')}")
        lines.append("")
        lines.append("Interpretation: where `margin fragile = YES`, upgrading from the developmental ridge estimator to the confirmatory MLP either flips whether the throughput-invariance delta stays inside `delta_eq` or moves it by more than half the band. For those families, `delta_eq` must be re-derived from the confirmatory estimator's empirical floor rather than carried over from ridge. This derisks the architecture upgrade without breaking the confirmatory holdout/blinding.")
        lines.append("")

    if figs:
        lines.append("## Figures")
        for fig in figs:
            lines.append(f"- [{fig}]({fig})")
        lines.append("")

    lines.append("## Required caution")
    lines.append("These runs do not establish human learning, LLM reliability in deployment, or superintelligence-scale auditability. They validate measurement logic and provide concrete gates for later confirmatory runs.")
    lines.append("")

    path = outdir / "report.md"
    path.write_text("\n".join(lines), encoding="utf-8")
    return path
